                  <h2 id="h-medications-for-gout"><strong>Medications For Gout</strong></h2>
                  <p>If home remedies aren’t cutting it, your provider may recommend you take an over-the-counter (OTC) medication to manage your pain.</p>
                  <h3 id="h-over-the-counter-medications">Over-the-Counter Medications</h3>
                  <p>Nonsteroidal anti-inflammatory drugs (NSAIDs) such as ibuprofen (Advil and Motrin) and <a href="#">naproxen</a> sodium (Aleve) can help to reduce inflammation associated with gout, and in turn, reduce the pain, swelling, and redness of a gout attack.</p>
                  <p>Your healthcare provider can recommend the best nonprescription medication for you to manage gout flare-ups.</p>
                  <h3 id="h-prescription-medications">Prescription Medications</h3>
                  <p>If home remedies and OTC medications aren’t working to manage your symptoms, your healthcare provider may recommend prescription medication for your gout.&nbsp;</p>
                  <p>This may include colchicine (Colcrys, Mitigare), or corticosteroids.&nbsp;</p>
